#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char	*tmp;
	int		i;

	i = 0;
	tmp = b;
	while (len != 0)
	{
		len--;
		tmp[i] = c;
		i++;
	}
	return (tmp);
}
