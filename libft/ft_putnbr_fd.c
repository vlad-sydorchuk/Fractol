#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	s[10];
	int		c;

	if (n == 0)
		write(fd, "0", 1);
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(fd, "-", 1);
		}
		c = 0;
		while (n > 0)
		{
			s[c++] = n % 10 + 48;
			n = n / 10;
		}
		while (--c >= 0)
			write(fd, &s[c], 1);
	}
}
