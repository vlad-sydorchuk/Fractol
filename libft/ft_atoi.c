int		ft_atoi(const char *str)
{
	int				sg;
	int				n;

	n = 0;
	sg = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
	{
		sg = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		n = n * 10 + *str++ - '0';
	if (sg == -1)
		n *= sg;
	return (n);
}
